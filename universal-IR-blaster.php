        <h2>Development of a Universal IR Blaster for Smart Device Control:</h2>
        <div class="detail-obj">
            <h4><b>Objectives:</b></h4>
            <p>The primary objective of this project is to design and develop a compact, cost-effective infrared (IR) blaster capable of transmitting IR signals to control a wide range of home appliances, such as televisions, air conditioners, and other devices that utilize IR remote controls.</p>
            <p>The device aims to be operated using microcontrollers or mobile phones, providing a versatile and user-friendly solution for home automation. By integrating advanced features and ensuring compatibility with various appliances, the universal IR blaster seeks to enhance the convenience and efficiency of controlling household devices.</p>
