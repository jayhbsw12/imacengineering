        <img src="./assets/case-study-images/IR-Blaster.webp" class="case-detail-image" alt="IR-Blaster">
       
        <div class="detail-obj">
            <h4><b>Challenges:</b></h4>
            <ul>
                <li><i class="fa fa-check bn-icon" aria-hidden="true"></i><b>Casing Assembly Complexity</b></li>
                <p>One of the significant challenges faced during the development was the difficulty in joining the top and bottom parts of the IR blaster enclosure. Initially, snap-fit joints were considered; however, they proved unreliable due to tight tolerances, leading to difficulties in assembly. Ultrasonic welding was also explored as an alternative joining method but was deemed too expensive for the project's budget. These challenges necessitated the exploration of cost-effective and efficient solutions to ensure the structural integrity and ease of assembly of the device.</p>
                <li><i class="fa fa-check bn-icon" aria-hidden="true"></i><b>Reset Button Integration</b></li>    
                <p> Integrating the reset button into the mold design presented its own set of challenges. The button's  position made it hard to access and complicated the injection molding process, increasing the chance of defects or misalignment. Ensuring that the reset button was both functional and easily accessible required careful consideration of the mold design and the overall user interface.</p>
            </ul>
        </div>

        <img src="./assets/case-study-images/IR-Blaster-(2).webp" class="case-detail-image" alt="IR-Blaster-(2)">
        
        <div class="detail-obj">
            <h4><b>Solution:</b></h4>
            <ul>
                <li><i class="fa fa-check bn-icon" aria-hidden="true"></i><b>Simplified Enclosure with Screws</b></li>
                <p>To overcome the casing assembly challenges, small screws were employed to join the top and bottom parts of the enclosure. This method proved to be budget-friendly, easy to assemble, and preserved the clean outer appearance of the product. The use of screws allowed for precise alignment and secure fastening, ensuring the structural integrity of the device without the need for specialized equipment or processes.</p>
                <li><i class="fa fa-check bn-icon" aria-hidden="true"></i><b>Redesigned Button Access</b></li>    
                <p>To address the reset button integration issue, the mold design was slightly modified to reposition the reset button in a more accessible location. Additionally, the wall thickness around the button was adjusted to facilitate a smoother molding process and ensure the button was easier to use without damaging the casing. These modifications improved the functionality and user experience of the device.</p>
            </ul>
        </div>
