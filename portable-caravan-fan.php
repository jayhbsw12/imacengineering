        <h2> Portable Caravan Fan</h2>
        <div class="detail-obj">
            <h4><b>Objectives:</b></h4>
            <p>The primary objective was to completely develop a new product using an existing caravan fan available in the market with upgraded features and enhanced user experience.</p>
            <p>The client desired a modern, more reliable, and user-friendly fan suitable for caravan use, with capabilities for remote control, improved rotation locking, battery protection, and adjustable speed and timing. Additionally, the new design needed to maintain compatibility with caravan environments, focusing on energy efficiency and operational safety.</p>
        </div>

        <img src="./assets/case-study-images/Portable-Caravan-Fan.webp" class="case-detail-image" alt="Portable-Caravan-Fan">

        <div class="detail-obj">
            <h4><b>Challenges:</b></h4>
            <p>The process revealed several mechanical and electronic limitations in the original design. The original fan used a basic button-operated control system and relied on mechanical spring plungers to prevent unwanted rotation during caravan movement. This presented wear-and-tear risks and limited user control. Another challenge was ensuring reliable battery performance, especially during prolonged operation, without draining the power source. Integrating new control features while maintaining a compact and robust design for caravan use required careful system redesign.</p>
        </div>

        <img src="./assets/case-study-images/Centrifuge-Machine-(3).webp" class="case-detail-image" alt="Centrifuge-Machine-(3)">

        <!-- <img src="./assets/case-study-images/NOX-Adviata.webp" class="case-detail-image"> -->
        <div class="detail-obj">
            <h4><b>Solution:</b></h4>
            <p>To overcome these challenges, the fan was redesigned with a full 360Â° motorized rotation mechanism, incorporating a remote-controlled system for enhanced convenience. The mechanical plungers were replaced with a user-friendly lever-based locking mechanism, allowing easy manual locking and unlocking of the rotating function.</p>
            <p>A timer feature was added with 2-hour, 4-hour, and 6-hour modes, offering battery-saving operation and customizable usage time. An undervoltage cut-off system was implemented to protect the caravan battery, automatically shutting off the fan when the voltage drops too low. LED indicators were included to display timer settings and variable speed levels, offering intuitive feedback to the user.</p>
        </div>
        <!-- <img src="assets/case-study/VivaE-working-.004.webp" class="case-detail-image"> -->
        <div class="detail-obj">
            <h4><b>Outcome:</b></h4>
            <p>The redesigned fan delivered significant improvements in functionality, safety, and user experience. The remote-control feature and enhanced locking mechanism provided easier operation, especially in mobile environments. The timer and battery protection systems ensured energy efficiency and prolonged battery life, which are critical in caravanning scenarios.</p>
            <p>The LED indicators offered real-time feedback, increasing ease of use. Overall, the project successfully transformed a simple mechanical fan into a smart, feature-rich solution tailored to modern caravan needs, meeting and exceeding client expectations.</p>
        </div>
    </div>
