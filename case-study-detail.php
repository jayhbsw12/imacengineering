            <h4><b>Challenges:</b></h4>
            <p>The redesign of the Nox Advaita product faced several critical engineering and integration challenges, particularly due to limited internal space, aesthetic constraints, and functional requirements. Below is a summary of the most significant hurdles encountered during development:</p>
            <p>One of the most complex challenges was concealing the hydraulic cylinder within the body without compromising accessibility. The design required the cylinder to be fully hidden for aesthetic reasons, yet still replaceable within 30 seconds for service or maintenance. This required innovative bracket design and removable panels that allow tool-free or quick-release access without exposing components in normal operation.</p>
            <p><b>The monitor rack posed a dual challenge:</b></p>
            <ul>
                <li><i class="fa fa-check bn-icon" aria-hidden="true"></i>The monitor’s heavy weight created stress on the upper body panel, causing structural deformation in earlier designs.</li>
                <li><i class="fa fa-check bn-icon" aria-hidden="true"></i>The available installation area was constrained to 450 mm × 450 mm, requiring a load-bearing solution within tight dimensional limits.</li>    
            </ul>
            <p>To solve this, we had to reinforce the mounting area internally without increasing outer dimensions or shifting the center of gravity, which was critical for balance and safe operation.</p>
            <p>Designing a simple, reliable mounting mechanism for the storage box cover was another challenge. It had to support repeated opening and closing, without using visible screws, and still be easy to assemble. The acrylic lid hinge also had to be chosen carefully to avoid cracking and to align aesthetically with the rest of the unit. This led to the need to source suitable friction hinges that were compact, durable, and provided smooth resistance.</p>
            <p>Fitting the lower structural plate was a challenge as we could not increase the overall dimensions of the product. The plate had to support internal components while still allowing access for servicing and maintain proper weight distribution to keep the center of gravity balanced.</p>
            <p>Fillet bends were introduced to improve visual softness and edge safety, but these bending radii created manufacturing difficulties, especially in mass production and tooling alignment. A balance had to be found between achieving the desired aesthetic with soft curves and the practicality of sheet metal fabrication.</p>
            <p>The team prioritized ease of assembly and maintenance by reducing part count and simplifying fastening methods. A specific challenge was meeting the requirement for minimum screws on visible surfaces, which limited conventional mounting strategies. This required concealed fasteners, snap-fit parts, and alternate bracket placements.</p>
            <p>These challenges collectively required creative mechanical design, careful material and hinge selection, and precise planning to ensure the product met both functional and aesthetic goals without compromising manufacturing feasibility. The solutions achieved a balance between serviceability, structural integrity, and user-focused design, all within a compact and visually clean product architecture.</p>
        </div>
        <img src="assets/case-study-images/NOX-Adviata.webp" class="case-detail-image">
        <div class="detail-obj">
            <h4><b>Solution:</b></h4>
            <p>The project involved a series of innovative design modifications aimed at enhancing functionality, safety, and aesthetics. A bracket with a locking knob was incorporated into the monitor rack to prevent the monitor from tipping over, ensuring stability during use.</p>
            <p>To improve user ergonomics, the movable arm's reach was extended, and a knob was added for easy adjustment, preventing it from obstructing the user's movement. Maintenance of the filter box was simplified by designing custom thumbscrews, allowing for tool-free access and easy replacement of filters. In the wheel assembly, a cylindrical nut with internal threads was welded onto the body, and the tire was secured with a screw, complemented by a spring washer to prevent the wheel from detaching during operation</p>
            <p>The base plate was engineered to support the entire assembly, ensuring structural stability. For efficient storage, the storage box was designed in three parts, facilitating precise bends and enhancing manufacturing efficiency while ensuring worker safety. Branding was applied by welding the logo onto the top surface and powder coating the entire assembly with the same color, achieving a professional appearance. To ensure smooth operation of the acrylic lid, a friction hinge was utilized, providing controlled movement and durability. These design solutions collectively improved the product's performance, safety, and aesthetic appeal.</p>
        </div>
