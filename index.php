      'updatedAt', 'blogDate' // seen in some payloads
   ];

   function parseFlexibleDate(v) {
      if (!v) return NaN;
      // ISO / RFC  let JS handle it
      let ts = Date.parse(v);
      if (!Number.isNaN(ts)) return ts;

      // Try DD/MM/YYYY or DD-MM-YYYY
      const m = /^(\d{1,2})[\/\-](\d{1,2})[\/\-](\d{4})$/.exec(String(v));
      if (m) {
         const [, d, mo, y] = m.map(Number);
         const dt = new Date(y, mo - 1, d);
         return +dt;
      }
      return NaN;
   }

   function getBlogTimestamp(blog) {
      for (const f of DATE_FIELDS) {
         if (blog && blog[f]) {
            const ts = parseFlexibleDate(blog[f]);
            if (!Number.isNaN(ts)) return ts;
         }
      }
      return -Infinity; // push undated items to the end
