        <div class="detail-obj">
            <h4><b>Outcome:</b></h4>
            <p>The redesign of the Nox Advaita unit successfully transformed its structure and aesthetics, aligning it with modern clinical and mobile requirements. Exposed components like the hydraulic cylinder and tire mounting nuts were concealed, presenting a cleaner and more professional appearance. Rounded corner fillets were introduced to all body edges, enhancing safety and providing a softer visual identity. A new filter box design was seamlessly integrated into the body profile, contributing to a cohesive look. Structural improvements were made by reinforcing mounting components prone to damage during transportation, ensuring robustness without compromising the unit's lightweight nature. Movable parts were precisely aligned to prevent contact with the body, reducing wear and enhancing durability.</p>
            <p>Functional integration was a key focus, with the monitor arm relocated from its external mount to an internal body integration, reducing clutter and improving protection. A built-in storage box was incorporated within the chassis, offering discreet and accessible storage without compromising space or exterior design. User-centric design principles were applied by optimizing weight and enhancing part durability, improving portability and usability. Branding was thoughtfully retained to maintain product recognition while aligning the design with current healthcare and professional standards. The redesigned Nox Advaita unit now delivers a cohesive, reliable, and visually refined product that addresses real-world usage challenges, enhances long-term durability, and meets the ergonomic and operational expectations of its users</p>
