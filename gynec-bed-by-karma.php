        <div class="detail-obj">
            <h4><b>Solution:</b></h4>
            <p>To address the complex mechanical and ergonomic challenges involved in developing a three-section adjustable bed, we implemented a series of targeted design solutions focused on structural stability, smooth actuation, and user accessibility.</p>
            <p>To accommodate the segmented configuration, the bed was designed to hold three separate mattresses, each aligned with the headrest, backrest, and legrest sections, ensuring user comfort and proper articulation during movement.</p>
            <p>For structural support, a small central stud was integrated into the base frame, enhancing rigidity and minimizing flex during dynamic operation. A custom strip bracket was introduced to secure the backrest frame, allowing consistent pivoting and improved load transfer during actuation.</p>
            <p>To ensure reliable motion of the backrest, a dedicated hydraulic actuator bracket was strategically placed to guide the actuator through its full range of motion without misalignment or binding. This placement was optimized for torque efficiency and structural balance.</p>
            <p>One of the key challenges—achieving controlled upward movement of the headrest—was solved by integrating a gas spring mechanism. This enabled a 40 degree upward incline from the horizontal plane, providing ergonomic neck and head support while maintaining smooth and adjustable resistance.</p>
            <p> In line with user and caregiver ergonomics, the bed frame was engineered to offer adjustable height functionality, ranging from a minimum of 500 mm to a maximum of 850 mm. This allows comfortable patient transfers for individuals with limited mobility, while also supporting caregivers in upright working positions. </p>
            <p>Each component was designed to maintain the center of gravity within a stable zone throughout all motion sequences, preventing imbalance or tipping. Bracket reinforcements and actuator mounts were strategically distributed to minimize structural complexity while maintaining durability and long-term performance under clinical conditions.</p>
            <p>These solutions collectively ensure the bed system meets both functional requirements and safety standards, while providing a user-friendly, modular platform adaptable for hospitals, assisted living, and home care environments.</p>
        </div>
