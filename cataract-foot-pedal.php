        <h2>Cataract Foot Pedal</h2>
        <div class="detail-obj">
            <h4><b>Objectives:</b></h4>
            <p>The objective of this project was to re-engineer and modernize an existing surgical foot pedal that had been in use for over three decades. The legacy design, although functional, was bulky, mechanically complex, and lacked the ergonomic and aesthetic appeal required in today’s competitive medical equipment market. The client initially approached us to upgrade the design while keeping its core functionality intact. However, as the project evolved, the scope expanded considerably.</p>
            <p>The client sought a next-generation foot pedal that was not only more compact and ergonomic but also aesthetically premium, cost-effective, and technologically robust. They wanted a product that could meet the rigorous demands of operating room use while also delivering enhanced user comfort, longer lifespan, and improved reliability. In addition, there was a need to localize component sourcing, reduce production costs, and integrate electronic safety monitoring features.</p>
        </div>

        <img src="./assets/case-study-images/Cataract-Foot-Padel.webp" class="case-detail-image" alt="Cataract Foot Pedal">

        <div class="detail-obj">
            <h4><b>Challenges:</b></h4>
            <p>The redesign process posed numerous challenges, both from a mechanical and user-centric perspective. The original product relied heavily on mechanical gears, linkages, and contact-based detection methods, which added to the size and complexity. These older systems were also prone to wear and required frequent maintenance, leading to downtime in critical medical environments.</p>
            <p>Several major issues had to be addressed:</p>
            <ul>
                <li><i class="fa fa-check bn-icon" aria-hidden="true"></i><b>Legacy Design Limitations: </b>The existing product was based on outdated mechanical engineering principles. Its complexity and bulk made it incompatible with the demands for compactness and ease of manufacturing.</li>
                <li><i class="fa fa-check bn-icon" aria-hidden="true"></i><b>Evolving Client Requirements: </b>As the project progressed, the client introduced multiple new requirements, including material changes, aesthetic refinements, functional additions, and numerous design iterations—over 15 to 20 major revisions in total. These evolving inputs necessitated an agile development process.</li>
                <li><i class="fa fa-check bn-icon" aria-hidden="true"></i><b>Functional Demands: </b>The new foot pedal had to be responsive from any angle, accommodate different foot sizes, and avoid stress points, particularly in the resting position. Additionally, the pedal had to be strong enough to withstand the full body weight of a surgeon without any risk of damage or deformation.</li>
                <li><i class="fa fa-check bn-icon" aria-hidden="true"></i><b>Tactile Response Issues: </b>The earlier version had buttons that were hard and uncomfortable to press, leading to fatigue and inaccuracy during extended procedures.</li>
                <li><i class="fa fa-check bn-icon" aria-hidden="true"></i><b>Sensor Limitations: </b>The previous system used Faraday-effect magnets for foot position detection, which were inexpensive but low in sensitivity and lifespan. Moreover, they required frequent recalibration and were affected by environmental magnetic interference.</li>
                <li><i class="fa fa-check bn-icon" aria-hidden="true"></i><b>Global Component Sourcing Constraints: </b>The client mandated local procurement for most components to reduce dependency on international suppliers, lower costs, and streamline logistics. However, certain specialized parts, like high-precision position monitoring chips, still had to be sourced internationally.</li>
            </ul>            
        </div>
        <div class="detail-obj">
            <h4><b>Solution:</b></h4>
            <p>Our solution began with a deep-dive engineering analysis of the existing product, identifying inefficiencies and opportunities for simplification and innovation. We then initiated a structured ideation and prototyping process, presenting the client with four to five conceptual designs. One design was selected based on its market relevance, enhanced functionality, and potential for refinement.</p>
             <div class="detail-goals">
            <h3><i>Key innovations included:</i></h3>
            <ul>
                <li><i class="fa fa-check bn-icon" aria-hidden="true"></i><b>Mechanical Simplification with Seesaw Pivot Joint: </b>To replace the legacy gear-based mechanisms, we introduced a simple yet robust seesaw-style pivot joint. This minimized the need for multiple mechanical linkages, drastically reducing the internal complexity, size, and failure points of the system.</li>
                <li><i class="fa fa-check bn-icon" aria-hidden="true"></i><b>Advanced Non-Contact Sensor Integration: </b>We replaced traditional Faraday magnets with AMR (Anisotropic Magneto-Resistive) sensors, enabling non-contact foot position detection. These sensors provide higher resolution, longer lifespan, and better reliability. For the magnetic source, we used rare earth magnets, known for their extreme strength and longevity (up to 100 years with only ~15% degradation). This allowed us to use smaller magnets (half-pea size) without compromising performance.</li>
                <li><i class="fa fa-check bn-icon" aria-hidden="true"></i><b>Improved Button Design: </b>The original push-button mechanisms were replaced with micro tactile push buttons, which offered a feather-touch experience with precise feedback. These buttons were meticulously calibrated to ensure optimal tactile response, reducing fatigue during long surgeries.</li>
                <li><i class="fa fa-check bn-icon" aria-hidden="true"></i><b>Ergonomic and Structural Redesign: </b>We redesigned the form factor of the foot pedal to ensure it accommodated natural foot angles, continuous motion, and all-angle actuation. We built in physical limiters and structural reinforcements to prevent breakage or mechanical failure—even if a surgeon accidentally stood on the pedal.</li>
                <li><i class="fa fa-check bn-icon" aria-hidden="true"></i><b>Electronic Monitoring System: </b>We embedded a compact position-monitoring chip within the housing to continuously track pedal activity and ensure safety compliance.</li>
                <li><i class="fa fa-check bn-icon" aria-hidden="true"></i><b>Procuring materials and components locally: </b>By procuring materials and components locally wherever possible, we drastically cut down on logistics and procurement costs. The redesigned mechanism also used fewer parts, further reducing the bill of materials and simplifying the assembly process.</li>
                <li><i class="fa fa-check bn-icon" aria-hidden="true"></i><b>Aesthetic and Market Considerations: </b>The external design was refined to offer a clean, sleek appearance, matching the look and feel of high-end medical devices. Surface textures, materials, and finishes were carefully selected to project a premium, upmarket impression while maintaining durability and cleanability.</li>
            </ul>
        </div>
        </div>
        <!-- <img src="assets/case-study/VivaE-working-.004.webp" class="case-detail-image"> -->
        <div class="detail-obj">
            <h4><b>Outcome:</b></h4>
            <p>The outcome of the project was a successfully re-engineered, next-generation surgical foot pedal that significantly surpassed the original in both form and function. The final design featured a compact and ergonomic structure, offering enhanced comfort and usability for surgeons during long procedures. Aesthetic upgrades provided a modern, premium look suitable for high-end medical environments. Internally, the mechanical complexity was reduced through streamlined engineering, resulting in improved reliability and ease of maintenance.</p>
            <p>Advanced electronic safety monitoring systems were integrated to ensure higher operational safety standards, and key components were localized to reduce costs and simplify the supply chain. Overall, the redesigned foot pedal delivered superior performance, longer service life, and better user experience, while aligning with the client's goals of technological innovation, cost efficiency, and market competitiveness.</p>
        </div>
