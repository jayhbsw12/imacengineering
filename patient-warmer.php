        <h2>Patient Warmer</h2>
        <div class="detail-obj">
            <h4><b>Objectives:</b></h4>
            <p>The client aimed to develop a next-generation surgical body warmer: highly compact, whisper-quiet, and inspired by the sleek designs seen in foreign medical devices—but distinctly original. It had to maintain a patient’s normothermia throughout all surgical stages, incorporating true HEPA-level filtration for clinical safety, modular and user-accessible maintenance, and a sustainable, profit-driving model through proprietary filter replacements.</p>
        </div>

        <img src="./assets/case-study-images/Patient-Warmer.webp" class="case-detail-image" alt="Patient Warmer">

        <div class="detail-obj">
            <h4><b>Challenges:</b></h4>
            <p>We faced several critical hurdles ,developing the warmer without violating patents, and dramatically reducing unit size without compromising efficiency. Incorporating HEPA-grade filtration—capable of at least 99.97% capture of particles ≥ 0.3 µm—was essential to mitigate surgical-site contamination risks. True HEPA filters dramatically outperform standard filters; studies show many forced-air warmers allow roughly 6–36% of sub‑0.5 µm particles to penetrate and even emit microbes from internal buildup. Finally, implementing a fully modular design, intuitive maintenance flow, and reliable triple-sensor safety logic added mechanical and control system complexity</p>
        </div>

        <img src="./assets/case-study-images/Patient-Warmer-(2).webp" class="case-detail-image" alt="Patient Warmer (2)">

        <!-- <img src="./assets/case-study-images/NOX-Adviata.webp" class="case-detail-image"> -->
        <div class="detail-obj">
            <h4><b>Solution:</b></h4>
            <p>Our team conducted several researches on , both Indian and international warmers to understand airflow, heating, and control mechanisms. We used a compact high-capacity blower and engineered a novel airflow pathway that sidestepped existing patents.</p>
            <p>To optimize portability, we integrated a collapsible hose absent in modern units. A vertical-side chassis houses a modular electronics tray and rear slide-out True-HEPA filter compartment, facilitating maintenance without turning over the unit. We installed three redundant sensors—patient contact, airflow rate, and internal diagnostics—for automatic shutdown if anomalies are detected. The system delivers low-noise airflow and prompts users to replace proprietary HEPA filters every 1,000 hours—both meeting OR safety standards.</p>
        </div>
        <!-- <img src="assets/case-study/VivaE-working-.004.webp" class="case-detail-image"> -->
        <div class="detail-obj">
            <h4><b>Outcome:</b></h4>
            <p>The finished surgical warmer is significantly smaller, quieter, and more cost-effective than traditional models  and Indian counterparts. It features modular internals for quick servicing, a space-saving collapsible hose, and state-of-the-art HEPA filtration to reduce contamination risk. Safety is further enhanced through automated shutdowns and filter change alerts.</p>
            <p>Clinically, it effectively maintains normothermia with the benefits of forced-air warming—shown to reduce operative hypothermia and surgical complications without increasing infection risk In summary, this device delivers patient-centered innovation, ease-of-use, and a strong commercial strategy, positioning our client at the forefront of surgical warming technology.</p>
        </div>
    </div>
