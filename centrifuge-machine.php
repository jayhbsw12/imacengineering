    <div class="container">
        <h2>Centrifuge Machine</h2>
        <div class="detail-obj">
            <h4><b>Objectives:</b></h4>
            <p>The goal of this project was to modernize the exterior appearance of an existing blood‑separation centrifuge without altering its core mechanics</p>
            <p>The client sought a fresh, automotive-inspired look—delivered at very low cost—while ensuring that the redesigned parts would be fully feasible for plain injection molding. We aimed to generate multiple initial concepts to capture the client’s vision and home in on a final design that could be validated through physical prototyping.</p>
        </div>
        <img src="./assets/case-study-images/Centrifuge-Machine-(2).webp" class="case-detail-image" alt="Centrifuge-Machine-(2)">
      
        <div class="detail-obj">
            <h4><b>Challenges:</b></h4>
            <p>We faced several constraints. The redesign had to integrate seamlessly with the existing machine geometry—no mechanical rework permitted. Budget constraints meant expensive finishes or tooling processes were off the table, so materials and production methods needed to remain simple.</p>
            <p> Additionally, the chosen concept had to meet strict manufacturability criteria for injection molding—draft angles, wall thicknesses, and toolability had to be carefully considered. Finally, achieving a sleek, automotive-style aesthetic while maintaining affordability and functional compatibility was a delicate balancing act.</p>
        </div>

        <img src="./assets/case-study-images/Centrifuge-Machine-(3).webp" class="case-detail-image" alt="Centrifuge-Machine-(3)">

        <!-- <img src="./assets/case-study-images/NOX-Adviata.webp" class="case-detail-image"> -->
        <div class="detail-obj">
            <h4><b>Solution:</b></h4>
            <p>We began by sketching ten distinct design concepts, each exploring a different visual direction—from minimalistic to sporty—to give the client options. From these, we narrowed the selection to three promising concepts based on client feedback.</p>
            <p>We then worked on an  existing assembly to capture precise dimensions and functional interface points, feeding those details into manufacturability studies. The top concept was modeled in 3D‑printing to test fit, form, and finish.</p>
            <p> This tangible prototype inspired additional detailing—such as buffer lines and trim accents drawn from car design influences. We iterated the design, refined the mold-ready geometry, and finalized a version optimized for injection molding, complete with proper draft, ribbing, wall thickness, and snap-fit features.</p>
        </div>
        <!-- <img src="assets/case-study/VivaE-working-.004.webp" class="case-detail-image"> -->
        <div class="detail-obj">
            <h4><b>Outcome:</b></h4>
            <p>The result achieved a striking visual refresh at minimal cost—no mechanical machining required. The final part met all criteria for low-cost injection molding and full production readiness.</p>
            <p> The client was impressed by the automotive qualities that emerged through iterative refinement, and   the 3D‑printed prototype accelerated decision-making and alignment on aesthetics.</p>
            <p> With the design now fully optimized for tooling, the project positions the client to move smoothly into volume production, delivering both aesthetic impact and cost efficiency.</p>
        </div>
    </div>
