            <h4><b>Challenges:</b></h4>
            <p>The project encountered several design challenges that required innovative solutions to enhance functionality and aesthetics. One significant issue was determining the optimal shape for the charging port cutout. Given that the charger is larger to support fast charging, a square cutout was necessary. However, the brand's triangular logo needed to be incorporated without compromising the charging port's accessibility. Achieving this balance required careful consideration of proportions and placement to maintain both brand identity and user convenience.</p>
            <p>Another challenge involved integrating the screen into the dashboard. With multiple screen options available, it was essential to select one that not only fit well within the dashboard's area but also complemented the overall design. This required evaluating various screen sizes and orientations to ensure optimal visibility and user interaction. Additionally, the mudguard design presented aesthetic challenges, as it needed to be both functional and visually appealing. </p>
            <p>The battery enclosure posed its own set of difficulties, as it had to meet IP69 standards for water and dust resistance while being housed within the footrest. This necessitated careful engineering to ensure the enclosure could withstand compression forces under 3 bar without compromising its integrity. Addressing these challenges led to a cohesive design that balanced functionality, safety, and brand identity.</p>           
